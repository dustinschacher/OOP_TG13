public class Dataobjekt {

    int x;
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    int y;
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }
    int klasse;
    public int getKlasse() {
        return klasse;
    }
    public void setKlasse(int klasse) {
        this.klasse = klasse;
    }
    public Dataobjekt(int x, int y, int klasse) {
        this.x = x;
        this.y = y;
        this.klasse = klasse;
    }

}

